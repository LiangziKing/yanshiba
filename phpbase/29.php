第48课 浮点数并不精确

10进制:3.1,是3*10^0+1*10^-1
3进制:1.1,是1*3^0+1*10^-1 // 对应10进制1.3333333....333...

10进制   2进制
0.5      0.1
0.8      0.1100..

某些小数在10进制下是有限的,转成2进制要无限循环,
因此,损失一些精度,导致浮点数计算和数学上的结果不一致
*/

